#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	t_uint	ct;

	ct = ft_strlen(s1);
	s1 += ct;
	while (*s2)
	{
		*(s1++) = *(s2++);
		ct++;
	}
	*s1 = '\0';
	return (s1 - ct);
}
