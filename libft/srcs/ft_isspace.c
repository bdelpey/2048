#include "libft.h"

int			ft_isspace(int c)
{
	t_uchar	ch;

	if (c < 0)
		ch = EOF;
	else
		ch = (t_uchar)c;
	return (ch == ' ' || (ch >= 011 && ch <= 015));
}
