#include <string.h>

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (s1 && s2)
	{
		while (*(s2 + i) && i < n)
		{
			*(s1 + i) = *(s2 + i);
			i += 1;
		}
		if (*(s2 + i) == '\0')
		{
			while (i < n)
			{
				*(s1 + i) = '\0';
				i += 1;
			}
		}
	}
	return (s1);
}
