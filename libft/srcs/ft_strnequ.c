#include <string.h>

int			ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!(s1 && s2))
		return (0);
	n--;
	while (*s1 && *s2 && n && *(s1) == *(s2))
	{
		s1 += 1;
		s2 += 1;
		n -= 1;
	}
	if (*(s1) == *(s2))
		return (1);
	else
		return (0);
}
